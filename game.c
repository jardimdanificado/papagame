DirtyList    papagaio_dirty;
uint8_t*     papagaio_fb;

static int t = 0;

void papagaio_init(void) {
    papagaio_sys.width = 160;
    papagaio_sys.height = 160;
    papagaio_sys.ram = 65536;
    papagaio_sys.vram = 65536;
}



void papagaio_update(void) {
    t++;

    // cria vários pequenos rects
    for (int r = 0; r < 10; r++) {
        int x0 = (t + r * 13) % (papagaio_sys.width - 20);
        int y0 = (t + r * 7) % (papagaio_sys.height - 20);

        int w = 20;
        int h = 20;

        for (int y = y0; y < y0 + h; y++) {
            for (int x = x0; x < x0 + w; x++) {
                papagaio_fb[y * papagaio_sys.width + x] = (x + y + t) & 0xFF;
            }
        }

        if (papagaio_dirty.count < MAX_DIRTY) {
            DirtyRect* d = &papagaio_dirty.rects[papagaio_dirty.count++];
            d->x = x0;
            d->y = y0;
            d->w = w;
            d->h = h;
            d->flags = 1;
        }
    }
}

